    <p class="ml-20p mb-1">一、本公司系統基於維護或轉換等例行性維護需要，而停止或縮短運作時間時，本公司將事前儘速通知用戶，不適用本條第二項規定。惟一年累計不得中斷服務超過四十八小時。超過該上限之時數，每十二小時扣減全月月租費三十分之一。</p>
    <p class="ml-20p mb-1">二、用戶租用本服務，如因可歸責於本公司之電信機線設備障礙、阻斷，以致發生錯誤、遲滯、中斷或不能傳遞致用戶連續十二小時以上無法連線使用時，其停止通信期間，當月月租費應依下列規定予以扣減，並於次期帳單扣抵。前項阻斷開始之時間，以本公司查覺或接到客戶通知之時間為準。但有事實足以證明實際開始阻斷之時間者，依實際開始阻斷之時間為準。用戶租用本服務，由於不可抗力致不能通信者，自連續阻斷屆滿三日之翌日起至修復日止，不收租費。</p>
