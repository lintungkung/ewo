    <p class="ml-20p mb-1">六、用戶如未於繳款截止日前繳付各項應付費用，如未繳交，本公司得以電話或語音或簡訊或書面等任一催繳方式通知用戶於期限內補繳；逾期仍未繳付時，本公司得暫停服務並繼續催繳，如仍未繳納時，本公司得逕行終止本契約，並得依法追繳，且要求用戶返還各項設備，若客戶不自行至本公司合作之有線電視系統經營區門市歸還各項設備，本契約終止後，本公司得以保證金充抵用戶積欠之各項費用，並就不足之金額追繳之。</p>
    <p class="ml-20p mb-1">七、用戶如因逾繳費期間而遭本公司拆除外線後，申請恢復連線服務時，除繳清欠款並繳交連線費用外，應再繳納纜線數據之設定費及裝機費後，方可復訊。</p>

