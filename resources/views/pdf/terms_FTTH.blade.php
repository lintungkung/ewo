    <p class="ml-20p mb-1">七、原合約期間已屆滿之用戶，欲參加年約優惠者，需以繳款單上所載期間優惠金額繳費，並同意遵守繳款單上年約特別約定事項及本合約之各項約定，如提前終止或違反本合約任一約定者，需補繳牌價金額，及返回贈品之等值現金。</p>
