    <p>第三條設備安裝及相關規定</p>
    <p class="ml-20p mb-1">一、為使本服務得以設立及運作，用戶明白並同意以下各點：</p>
    <p class="ml-30p mb-1">（一）本公司得選定協力廠商配合本公司服務人員至用戶之指定裝機地點（以下簡稱『指定地點』）架設及安裝纜線及數據機。</p>
    <p class="ml-30p mb-1">（二）用戶必須自行具備相關設備，包括但不限於個人電腦及具有合法版權之相容作業系統等各項設備在內。</p>
    <p class="ml-20p mb-1">二、 進入許可</p>
    <p class="ml-30p mb-1">（一）用戶同意本公司之服務人員得因裝機、檢查、測試、維修、變更、訪談、調查或移動各項設備及器材等目的，進入用戶指定地點進行上述及其他必要行為，但所有行為皆必須在用戶同意之時間內進行。</p>
    <p class="ml-30p mb-1">（二）用戶保證其有權同意本公司之服務人員進入指定地點進行上述及其他必要行為。倘若用戶並無權為上述之同意時，用戶同意賠償本公司因用戶無權為上述許可或為不實之上述許可及相關原因所遭受之一切損害及相關費用。</p>
    <p class="ml-30p mb-1">（三）用戶同意於本公司履行本契約之必要範圍內，得無償利用用戶之土地、建物、管道設施等佈設纜線或各項設備以順利提供本服務。</p>
    <p class="ml-20p mb-1">三、重置或變更各項設備</p>
    <p class="ml-20p mb-1">用戶不得重新裝置或變更有關本服務之各項設備，但用戶得要求本公司及本公司之協力廠商進行，但用戶須支付上述重置或變更各項設備所發生之一切相關費用，如若因服務範圍所限或用戶提供之書面資料不完整而無法更換時，本公司得不予受理。</p>
    <p class="ml-20p mb-1">四、數據機等各項設備之所有權及歸還</p>
    <p class="ml-30p mb-1">(一)  有關本服務之各項設備皆屬本公司所有，經本公司派員於用戶指定處安裝並經用戶確認已依用戶同意之方式進行線路牽引，完成安裝。用戶應盡善良管理人之注意義務保管各項設備，且用戶同意配合警調警察機關辦案需求，僅於用戶申裝地址內使用數據機等各項設備，用戶不得出售、轉讓、租賃或遷移全部或部分設備。</p>
    <p class="ml-30p mb-1">(二)  用戶如欲終止使用本服務時，用戶需自行至本公司合作之有線電視門市返還各項設備，於確認未損壞後，本公司始將原保證金退回。如用戶未返還或各項設備全部或部分之遺失、遭竊及損害或因用戶違反上述規定而發生一切有關之損失，用戶應負責賠償本公司。</p>
    <p class="ml-30p mb-1">(三） 使用本服務期間，因用戶使用不當致發生數據機(保固期間除外)或各項設備故障，用戶應立即通知本公司派員維修，本公司有權酌收維修費及車馬費，其金額依本公司另行訂定之維修服務收費標準，並視實際修復情形定之，前述費用由用戶當場以現金支付。</p>
