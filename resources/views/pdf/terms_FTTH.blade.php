    <p>第五條 繳費方式及變更</p>
    <p class="ml-20p mb-1">一、用戶於收到帳單後，應於帳單所載之繳費期限內，依雙方所約定或帳單上所記載之方式以現金繳納，雙方並同意月繳制者每月繳費一次；年繳制、半年繳制、季繳制及雙月繳，則為每週期始月份當月預繳該週期之所有費用。</p>
    <p class="ml-20p mb-1">二、付費方式之變更：用戶原約定之繳費方式如為【金融機構自動轉帳】或【信用卡自動代繳】，而欲變更為其他付款方式，或其他付款方式欲變更為【金融機構自動轉帳】或【信用卡自動代繳】者，應依帳單或申請本服務所填具表單中所載明之期限前通知本公司，並協同辦理相關程序，程序完備者該變更將於四十五個工作天之內生效，付款方式變更生效日前，用戶仍應依原約定之付款方式支付應付予本公司之所有款項。</p>
    <p class="ml-20p mb-1">三、資費方案之變更：如用戶有變更原資費方案之需，應依帳單或申請本服務所填具表單中所載明之方式向本公司提出申請。該項變更於本公司收到申請書之次月生效。</p>
    <p class="ml-20p mb-1">四、未能依帳單或申請本服務所填具表單中所載明之期限前為付費方式及資費方案變更之通知者，其變更程序將順延至次月處理。</p>

    <p>第六條本服務停用、欠費、終止與退費</p>
    <p class="ml-20p mb-1">一、使用月繳制之用戶終止本服務時，應於預定終止日之三個工作天前以書面或傳真向本公司申請，並於申請書面到達本公司後三個工作日內/或用戶指定之日(仍須有三個以上之工作日)開始生效。</p>
    <p class="ml-20p mb-1">二、採預付雙月繳制/季繳制/半年繳制/年繳制之用戶及適用優惠方案之用戶若欲申請期前終止時，就已繳連線費及其他相關費用不得請求退款。</p>
